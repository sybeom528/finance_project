    "    '''",
    "    def __init__(self, input_size, hidden_size, num_layers=3, dropout=0.2):",
    "        super().__init__()",
    "        self.lstm = nn.LSTM(",
    "            input_size=input_size,",
    "            hidden_size=hidden_size,",
    "            num_layers=num_layers,",
    "            batch_first=True,",
    "            dropout=dropout,",
